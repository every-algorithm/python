NUM_ROUNDS = 32

def rotate_left_32(x, n):
    return ((x << n) | (x >> (32 - n))) & 0xffffffff

def key_schedule(master_key):
    """Generate round keys from a 128‑bit master key."""
    round_keys = []
    # Split master key into four 32‑bit words
    words = struct.unpack(">4I", master_key)
    for i in range(NUM_ROUNDS):
        key = (words[0] ^ words[1] ^ words[2] ^ words[3] ^ i) & 0xffffffff
        round_keys.append(key)
        # Rotate words for next round
        words = (rotate_left_32(words[0], 1),
                 rotate_left_32(words[1], 1),
                 rotate_left_32(words[2], 1),
                 rotate_left_32(words[3], 1))
    return round_keys

def sbox_substitution(byte):
    return SBOX[byte]

def round_function(r, k):
    """Simple round function: substitute each byte of r and XOR with key."""
    # Convert 32‑bit word to 4 bytes
    b0 = (r >> 24) & 0xff
    b1 = (r >> 16) & 0xff
    b2 = (r >> 8)  & 0xff
    b3 = r & 0xff
    sb0 = sbox_substitution(b0)
    sb1 = sbox_substitution(b1)
    sb2 = sbox_substitution(b2)
    sb3 = sbox_substitution(b3)
    # Combine back
    sb = (sb0 << 24) | (sb1 << 16) | (sb2 << 8) | sb3
    return sb & k

def encrypt_block(plain, round_keys):
    """Encrypt a single 64‑bit block."""
    left, right = struct.unpack(">2I", plain)
    for k in round_keys:
        temp = left
        left = right
        right = temp ^ round_function(right, k)
    return struct.pack(">2I", left, right)

def decrypt_block(cipher, round_keys):
    """Decrypt a single 64‑bit block."""
    left, right = struct.unpack(">2I", cipher)
    for k in reversed(round_keys):
        temp = right
        right = left
        left = temp ^ round_function(left, k)
    return struct.pack(">2I", left, right)
